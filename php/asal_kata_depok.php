                            masa Cornelis Chastelein. Namun demikian, istilah tersebut bukan merupakan akronim Depok yang sebenarnya. 
                            Menurut Para Penutur, istilah itu dibentuk oleh komunitas orang-orang Depok yang hijrah ke Belanda setelah 
                            peristiwa Gedoran Depok. Istilah lain yang juga dibuat dengan akronim Depok yaitu “Daerah Pemukiman Orang Kota” 
                            seiring dengan dibangunnya Perumnas Depok sebagai tempat pemukiman orang-orang yang bekerja di ibukota.
